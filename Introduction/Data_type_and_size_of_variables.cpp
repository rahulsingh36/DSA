#include <bits/stdc++.h>
using namespace std;

int main(){
    int a = 1;
    cout<< a <<endl;
    cout<< "size of integer is " << sizeof(a)<<endl;

    char c = 'a';
    cout<< c <<endl;
    cout<< "size of character is " << sizeof(c)<<endl;

    bool b = true;
    cout<< b <<endl;
    cout<< "size of bool is " << sizeof(b)<<endl;

    float f = 1.2;
    cout<< f <<endl;
    cout<< "size of float is " << sizeof(f)<<endl;

    double d = 1.21;
    cout<< d <<endl;
    cout<< "size of double is " << sizeof(d)<<endl;

}